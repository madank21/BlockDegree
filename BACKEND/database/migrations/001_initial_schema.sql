-- TABLE: verification_logs
-- ─────────────────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS verification_logs (
  id                  UUID          PRIMARY KEY DEFAULT uuid_generate_v4(),
  computed_hash       VARCHAR(64),
  result              VARCHAR(20)   NOT NULL
                        CHECK (result IN ('valid','invalid','revoked')),
  verifier_id         UUID          REFERENCES users(id) ON DELETE SET NULL,
  verifier_ip         VARCHAR(45),
  degree_id           UUID          REFERENCES degrees(id) ON DELETE SET NULL,
  verification_method VARCHAR(20)
                        CHECK (verification_method IN ('hash','qr','ocr','face')),
  on_chain_record     JSONB         DEFAULT '{}',
  metadata            JSONB         DEFAULT '{}',
  verified_at         TIMESTAMPTZ   NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_vlog_hash      ON verification_logs(computed_hash);
CREATE INDEX IF NOT EXISTS idx_vlog_degree    ON verification_logs(degree_id);
CREATE INDEX IF NOT EXISTS idx_vlog_result    ON verification_logs(result);
CREATE INDEX IF NOT EXISTS idx_vlog_ip        ON verification_logs(verifier_ip);
CREATE INDEX IF NOT EXISTS idx_vlog_date      ON verification_logs(verified_at DESC);

