CREATE TABLE IF NOT EXISTS documents (
  id                    UUID          PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id               UUID          REFERENCES users(id) ON DELETE CASCADE NOT NULL,
  degree_id             UUID          REFERENCES degrees(id) ON DELETE SET NULL,
