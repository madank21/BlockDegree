-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: fraud_logs
-- ─────────────────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS fraud_logs (
  id              UUID          PRIMARY KEY DEFAULT uuid_generate_v4(),
  file_name       VARCHAR(500),
  file_hash       VARCHAR(64),
  fraud_score     INTEGER       NOT NULL DEFAULT 0
                    CHECK (fraud_score BETWEEN 0 AND 100),
  risk_level      VARCHAR(20)   NOT NULL DEFAULT 'MINIMAL'
                    CHECK (risk_level IN ('MINIMAL','LOW','MEDIUM','HIGH','CRITICAL')),
  is_fraudulent   BOOLEAN       NOT NULL DEFAULT false,
  findings        JSONB         NOT NULL DEFAULT '[]',
  breakdown       JSONB         DEFAULT '{}',
  yolo_detections JSONB         DEFAULT '{}',
  ocr_confidence  DECIMAL(5,2),
  reported_by     UUID          REFERENCES users(id) ON DELETE SET NULL,
  degree_id       UUID          REFERENCES degrees(id) ON DELETE SET NULL,
  created_at      TIMESTAMPTZ   NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_fraud_flag   ON fraud_logs(is_fraudulent);
CREATE INDEX IF NOT EXISTS idx_fraud_risk   ON fraud_logs(risk_level);
CREATE INDEX IF NOT EXISTS idx_fraud_date   ON fraud_logs(created_at DESC);

