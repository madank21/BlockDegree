-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: users
-- ─────────────────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS users (
  id                    UUID          PRIMARY KEY DEFAULT uuid_generate_v4(),
  name                  VARCHAR(200)  NOT NULL,
  email                 VARCHAR(255)  UNIQUE NOT NULL,
  password_hash         VARCHAR(255)  NOT NULL,
  role                  VARCHAR(20)   NOT NULL
                          CHECK (role IN ('admin','university','student','employer')),

  -- Student fields
  student_id            VARCHAR(100),

  -- University fields
  institution_id        UUID,
  institution_name      VARCHAR(200),

  -- Wallet (MetaMask MFA — optional)
  wallet_address        VARCHAR(42),

  -- Face verification
  -- Stored as JSON string of float[128]
  -- Selected explicitly only when needed — never auto-returned
  face_descriptor       TEXT,
  face_descriptor_hash  VARCHAR(64),
  face_registered_at    TIMESTAMPTZ,

  -- Account state
  is_active             BOOLEAN       NOT NULL DEFAULT true,
  email_verified        BOOLEAN       NOT NULL DEFAULT false,
  avatar_url            TEXT,
  last_login            TIMESTAMPTZ,
  metadata              JSONB         DEFAULT '{}',

  created_at            TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
  updated_at            TIMESTAMPTZ   NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_users_email       ON users(email);
CREATE INDEX IF NOT EXISTS idx_users_role        ON users(role);
CREATE INDEX IF NOT EXISTS idx_users_student_id  ON users(student_id);
CREATE INDEX IF NOT EXISTS idx_users_institution ON users(institution_id);

-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: degrees
-- ─────────────────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS degrees (
  id                      UUID          PRIMARY KEY DEFAULT uuid_generate_v4(),

  -- Student information
  student_name            VARCHAR(200)  NOT NULL,
  student_id              VARCHAR(100)  NOT NULL,
  graduate_id             UUID          REFERENCES users(id) ON DELETE SET NULL,
  graduate_email          VARCHAR(255),

  -- Degree details
  degree_title            VARCHAR(300)  NOT NULL,
  field_of_study          VARCHAR(300)  NOT NULL,
  graduation_date         DATE          NOT NULL,
  gpa                     DECIMAL(4,2)  CHECK (gpa >= 0 AND gpa <= 4.0),
  honors                  VARCHAR(100),
  metadata                JSONB         DEFAULT '{}',

  -- Institution
  institution_id          UUID          NOT NULL,
  institution_name        VARCHAR(200),
  issued_by               UUID          REFERENCES users(id) ON DELETE SET NULL,

  -- ── CRYPTOGRAPHIC SPINE ───────────────────────────────────────────────────
  -- SHA-256 of normalised credential fields.
  -- IMMUTABLE after issuance. This is what gets stored on blockchain.
  degree_hash             VARCHAR(64)   UNIQUE NOT NULL,

  -- Certificate number (auto-generated by trigger)
  certificate_number      VARCHAR(100)  UNIQUE,

  -- Lifecycle status
  status                  VARCHAR(20)   NOT NULL DEFAULT 'pending'
                            CHECK (status IN ('pending','issued','revoked','suspended')),

  -- Blockchain synchronisation tracking
  blockchain_sync_status  VARCHAR(20)   NOT NULL DEFAULT 'queued'
                            CHECK (blockchain_sync_status IN
                              ('queued','processing','success','failed','queue_error')),
  blockchain_tx_hash      VARCHAR(66),
  blockchain_block_number BIGINT,
  blockchain_timestamp    BIGINT,

  -- Revocation
  revocation_reason       TEXT,
  revoked_at              TIMESTAMPTZ,
  revocation_tx_hash      VARCHAR(66),

  -- QR code (Base64 DataURL — cached after first generation)
  qr_code_url             TEXT,

  -- IPFS — decentralised document storage
  -- Answers the examiner question: "What if Supabase is compromised?"
  -- The degree PDF lives on IPFS. The CID is stored on the blockchain.
  ipfs_cid                VARCHAR(200),
  ipfs_gateway_url        TEXT,
  document_hash           VARCHAR(64),
  document_url            TEXT,

  created_at              TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
  updated_at              TIMESTAMPTZ   NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_degrees_student_id      ON degrees(student_id);
CREATE INDEX IF NOT EXISTS idx_degrees_degree_hash     ON degrees(degree_hash);
CREATE INDEX IF NOT EXISTS idx_degrees_institution_id  ON degrees(institution_id);
CREATE INDEX IF NOT EXISTS idx_degrees_status          ON degrees(status);
CREATE INDEX IF NOT EXISTS idx_degrees_bc_sync         ON degrees(blockchain_sync_status);
CREATE INDEX IF NOT EXISTS idx_degrees_graduate_id     ON degrees(graduate_id);
CREATE INDEX IF NOT EXISTS idx_degrees_created_at      ON degrees(created_at DESC);

-- Auto-generate certificate number on insert
CREATE SEQUENCE IF NOT EXISTS certificate_seq START 100000 INCREMENT 1;

CREATE OR REPLACE FUNCTION generate_certificate_number()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.certificate_number IS NULL THEN
    NEW.certificate_number :=
      'BD-' || TO_CHAR(NOW(), 'YYYY') || '-' ||
      LPAD(NEXTVAL('certificate_seq')::TEXT, 6, '0');
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_cert_number ON degrees;
CREATE TRIGGER trg_cert_number
  BEFORE INSERT ON degrees
  FOR EACH ROW EXECUTE FUNCTION generate_certificate_number();

-- Auto-update updated_at
CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_degrees_updated_at ON degrees;
CREATE TRIGGER trg_degrees_updated_at
  BEFORE UPDATE ON degrees
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();

DROP TRIGGER IF EXISTS trg_users_updated_at ON users;
CREATE TRIGGER trg_users_updated_at
  BEFORE UPDATE ON users
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();

